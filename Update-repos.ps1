#This script is intended to be run from the root of a folder containing multiple git repos.  It will check each subfolder for a .git folder and if found, issue a git pull command to update the repo.

#Check the commandline for the path to a folder.  If not provided, prompt the user.
param (
    [string]$folder = $(Read-Host 'Enter the path to the folder containing the git repos. Press enter for the current folder.')
)
$currentLocation = Get-Location
if ($folder) {
    $Location = $folder
    Write-Output "Processing items in $Location"
}
else {
    $Location = $currentLocation
    Write-Output "Processing items in current folder, $Location"
}

$LogFolder = 'C:\ProgramData\PWSLogs'
$LogFile = 'Update-repos.log'
If (Test-Path $LogFolder) {
    Write-Output "$LogFolder exists.  Creating/appending to $LogFile."
}
else {
    Write-Output "The folder $LogFolder doesn't exist. This folder will be used for storing logs created after the script runs. Creating now."
    Start-Sleep 1
    New-Item -Path $LogFolder -ItemType Directory | Out-Null
    Write-Output "The folder $LogFolder was successfully created. Files will be written to $LogFile."
}
Start-Transcript -Append -IncludeInvocationHeader -Path "$LogFolder\$LogFile"

$repos = Get-ChildItem -Directory -Path $Location
$repoCount = 0

foreach ($repo in $repos) {
    Write-Output "Checking $repo"
    $gitFolder = Join-Path $repo.FullName '.git'
    if (Test-Path $gitFolder) {
        $repoCount++
        Write-Host "Updating $repo"
        Set-Location $repo.FullName
        git pull -a
    }
    else {
        Write-Host "No git repo found in $repo"
    }
}
Set-Location $currentLocation
Write-Output 'Done'
Write-Output "Processed $repoCount repos"

Stop-Transcript
