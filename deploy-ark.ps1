

param (
    [string]$machineName
)

#Prompt the user for the machine name if no machine name is passed on the commandline.
if ($args.Length -eq 0) {
    #Keep prompting the user until a machine name is entered.
    while ($machineName -eq '') {
        $machineName = Read-Host -Prompt 'Enter the machine name'
    }
}
else {
    $machineName = $args[0]
}


$location = 'East US'
$resourceGroup = 'ARK'
$subscription = '8a89e116-824d-4eeb-8ef4-16dcc1f0959b'
$port = '6516'
$portint = 6516
        
#Deploy Windows Admin Center
$Setting = @{'port' = $port; } #proxy configuration is optional
Write-Output "Deploying Windows Admin Center extension to $machineName in resource group $resourceGroup"
New-AzConnectedMachineExtension -Name 'AdminCenter' -ResourceGroupName $resourceGroup -MachineName $machineName -Location $location -Publisher 'Microsoft.AdminCenter' -Settings $Setting -ExtensionType 'AdminCenter' -SubscriptionId $subscription -Verbose
        
#Allow connectivity
$putPayload = "{'properties': {'type': 'default'}}"
Invoke-AzRestMethod -Method PUT -Uri "https://management.azure.com/subscriptions/${subscription}/resourceGroups/${resourceGroup}/providers/Microsoft.HybridCompute/machines/${machineName}/providers/Microsoft.HybridConnectivity/endpoints/default?api-version=2023-03-15" -Payload $putPayload

$patch = @{ 'properties' = @{ 'serviceName' = 'WAC'; 'port' = $portint } } 
$patchPayload = ConvertTo-Json $patch 
Invoke-AzRestMethod -Method PUT -Path /subscriptions/${subscription}/resourceGroups/${resourceGroup}/providers/Microsoft.HybridCompute/machines/${machineName}/providers/Microsoft.HybridConnectivity/endpoints/default/serviceconfigurations/WAC?api-version=2023-03-15 -Payload $patchPayload
