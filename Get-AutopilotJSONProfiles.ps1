#Receive the target folder as a commandline argument. 
param (
    [string]$targetDirectory
)

#If nothing is passed on the commandline, prompt the user to enter a target, or to press enter for the default path.
if (-not $targetDirectory) {
    $targetDirectory = Read-Host -Prompt 'Enter the target directory for the autopilot profiles or press enter for the current folder.'
    if (-not $targetDirectory) {
        $targetDirectory = '.\Autopilot'
    }
}
#Check to see if the microsoft.graph module is installed in the user or the machine context.  If not, install it.
Write-Output 'Checking for the Microsoft.Graph.Authentication module'

$InstalledModules = Get-InstalledModule -Name 'Microsoft.Graph.Authentication' -ErrorAction SilentlyContinue

if (!($InstalledModules)) {
    Write-Output 'Microsoft.Graph.Authentication module not found.  Installing...'
    Install-Module -Name 'Microsoft.Graph' -Scope CurrentUser -Force -AllowClobber
}
else {
    Write-Output 'Microsoft.Graph.Authentication module found. Moving on'
}


Connect-MgGraph -Scopes 'Device.ReadWrite.All', 'DeviceManagementManagedDevices.ReadWrite.All', 'DeviceManagementServiceConfig.ReadWrite.All', 'Domain.ReadWrite.All', 'Group.ReadWrite.All', 'GroupMember.ReadWrite.All', 'User.Read' -NoWelcome
$AutopilotProfile = Get-AutopilotProfile
$targetDirectory = '.\Autopilot'
#Check if the target directory exists, if so ask if you want to override it
if (Test-Path $targetDirectory) {
    $override = Read-Host -Prompt 'The target directory already exists. Do you want to override it? (Y/N)'
    if ($override -eq 'Y') {
        Remove-Item -Path $targetDirectory -Recurse -Force
    }
    else {
        Write-Output 'Exiting script'
        exit
    }
}
Write-Output "Processing $($AutopilotProfile.Count) autopilot profiles"
$AutopilotProfile | ForEach-Object {
    Write-Output "Converting $($_.displayName) to JSON"
    New-Item -ItemType Directory -Path "$targetDirectory\$($_.displayName)" | Out-Null
    Write-Output "Saving $($_.displayName) to $targetDirectory\$($_.displayName)\AutopilotConfigurationFile.json"
    $_ | ConvertTo-AutopilotConfigurationJSON | Set-Content -Encoding Ascii "$targetDirectory\$($_.displayName)\AutopilotConfigurationFile.json"
}
Write-Output "Processed $($AutopilotProfile.Count) autopilot profiles"
Write-Output "Do not forget to add the line '"CloudAssignedRegion": "os-default"' below to your JSON file."
